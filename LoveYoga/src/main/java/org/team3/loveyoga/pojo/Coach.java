package org.team3.loveyoga.pojo;

public class Coach {
    private Integer yuId;

    private String yuTel;

    private String yuPassword;

    private String yuName;

    private String yuEmail;

    private Integer flag;

    public Integer getYuId() {
        return yuId;
    }

    public void setYuId(Integer yuId) {
        this.yuId = yuId;
    }

    public String getYuTel() {
        return yuTel;
    }

    public void setYuTel(String yuTel) {
        this.yuTel = yuTel;
    }

    public String getYuPassword() {
        return yuPassword;
    }

    public void setYuPassword(String yuPassword) {
        this.yuPassword = yuPassword;
    }

    public String getYuName() {
        return yuName;
    }

    public void setYuName(String yuName) {
        this.yuName = yuName;
    }

    public String getYuEmail() {
        return yuEmail;
    }

    public void setYuEmail(String yuEmail) {
        this.yuEmail = yuEmail;
    }

    public Integer getFlag() {
        return flag;
    }

    public void setFlag(Integer flag) {
        this.flag = flag;
    }
}